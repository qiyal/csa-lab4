public class Color {
    public int cyan;
    public int magenta;
    public int yellow;
    public int black;

    public Color() {}

    @Override
    public String toString() {
        return "Color[" +
                "cyan=" + cyan +
                ", magenta=" + magenta +
                ", yellow=" + yellow +
                ", black=" + black +
                ']';
    }
}
